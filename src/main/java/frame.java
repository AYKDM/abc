    private void search(String fullname){
        list.clear();
        Connection con = null;
        Statement stm = null;
        try {
            con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/test","root","");
            String sql = "select * from student where name = '"+fullname+"'";
            stm = (Statement) con.createStatement();
            ResultSet set = stm.executeQuery(sql);
            while (set.next()){
                student s = new student(set.getInt("id"),
                        set.getString("name"),
                        set.getString("address"),
                        set.getString("email"));
                list.add(s);
            }
        }catch (Exception e){
e.printStackTrace();
        }finally {
            try {
                con.close();
                stm.close();
            }catch (Exception e){

            }
        }
    }
