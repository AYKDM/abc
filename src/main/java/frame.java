    private void insertcon(student s){
        Connection con = null;
        PreparedStatement pstm = null;
        try {
            con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/test","root","");
            String sql = "insert into student(name,address,email) values (?,?,?)";
            pstm = (PreparedStatement) con.prepareStatement(sql);
            pstm.setString(1,s.fullname);
            pstm.setString(2,s.address);
            pstm.setString(3,s.email);
            pstm.execute();
        }catch (Exception e){
e.printStackTrace();
        }finally {
                try {
                    con.close();
                    pstm.close();
                }catch (Exception e){
                    e.printStackTrace();
                }
        }
    }
